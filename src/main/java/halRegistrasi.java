    private boolean cekForm() {
        if (namaTF.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama harus diisi.");
            namaTF.requestFocus(); // Memfokuskan kembali ke textfield
            return false;
        }
        if (emailTF.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Email harus diisi.");
            emailTF.requestFocus(); // Memfokuskan kembali ke textfield
            return false;
        }
        if (passwordPF.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Password harus diisi.");
            passwordPF.requestFocus(); // Memfokuskan kembali ke textfield
            return false;
        }
        return true; // Jika semua field terisi
    }
    
