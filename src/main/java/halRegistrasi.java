    private void registrasiBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrasiBActionPerformed
        // TODO add your handling code here:
        
        if(!cekForm()){
            return;
        }
        
        String nama = namaTF.getText();
        String email = emailTF.getText();
        String password = new String(passwordPF.getPassword());
        
        try(Connection conn = Koneksi.connect()) {
            String query = "INSERT INTO pengguna (nama, email, password) VALUES (?, ?, ?)";
            PreparedStatement registrasi = conn.prepareStatement(query);
            registrasi.setString(1, nama);
            registrasi.setString(2, email);
            registrasi.setString(3, password);
            int rowAffected = registrasi.executeUpdate();
            
            if(rowAffected > 0) {
                JOptionPane.showMessageDialog(this, "Registrasi berhasil, silahkan login!!");
                halLogin login = new halLogin();
                login.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Registrasi gagal, silahkan coba lagi!!0");
            }
        } catch(SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan, silahkan isi form dengan benar!!");
        }
        
    }//GEN-LAST:event_registrasiBActionPerformed

